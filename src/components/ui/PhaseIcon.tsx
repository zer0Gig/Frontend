export function PhaseIcon({ phase, size = 14, className = "" }: PhaseIconProps) {
  const render = icons[phase] || fallback;
  return <span className={`inline-flex items-center justify-center flex-shrink-0 ${className}`}>{render(size)}</span>;
}

/** Returns a Tailwind color class based on phase type */
export function phaseColor(phase: string): string {
  if (["completed", "brief_downloaded"].includes(phase))          return "text-emerald-400";
  if (["processing", "processing_fallback"].includes(phase))      return "text-cyan-400";
  if (["self_review"].includes(phase))                            return "text-violet-400";
  if (["memory_loaded", "memory_saved"].includes(phase))          return "text-blue-400";
  if (["waiting_approval"].includes(phase))                       return "text-amber-400";
  if (["uploading", "uploaded", "agent_output"].includes(phase))  return "text-sky-400";
  if (["submitting"].includes(phase))                             return "text-indigo-400";
  if (["error", "brief_fallback", "upload_fallback"].includes(phase)) return "text-red-400";
  return "text-white/40";
}
